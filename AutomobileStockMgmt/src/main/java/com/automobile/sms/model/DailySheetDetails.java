package com.automobile.sms.model;

public class DailySheetDetails {

	private String particulars;

	private String quantity;

	private String billNo;

	private String amount;

	private String dateOfExpenditure;

	private String expenditureType;

	

	public String getParticulars() {
		return particulars;
	}

	public void setParticulars(String particulars) {
		this.particulars = particulars;
	}

	public String getQuantity() {
		return quantity;
	}

	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}

	public String getBillNo() {
		return billNo;
	}

	public void setBillNo(String billNo) {
		this.billNo = billNo;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getDateOfExpenditure() {
		return dateOfExpenditure;
	}

	public void setDateOfExpenditure(String dateOfExpenditure) {
		this.dateOfExpenditure = dateOfExpenditure;
	}

	public String getExpenditureType() {
		return expenditureType;
	}

	public void setExpenditureType(String expenditureType) {
		this.expenditureType = expenditureType;
	}

}
